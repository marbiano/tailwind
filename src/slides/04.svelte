<h1>About the opportunity</h1>
<p>By now, it should be clear why I wrote this. But there’s more.</p>
<p>
  These days, I’m looking into building fewer and fewer individual websites and
  more into creating tools that help to build websites. To go up one layer. For
  that, I’ve been working on leveling up my Node skills to be able to
  contribute. It’s a train I don’t want to miss.
</p>
<p>
  Tailwind CSS is already a significant player in this area and feels to me like
  Tailwind UI could become a huge thing . With the kind of audience that you
  got, nothing would stop you from writing your own dynamic site generator, or
  replace PostCSS with a parser that suits your needs in a more tailor-made way.
  The sky seems to be the limit.
</p>
<p>That’s the kind of challenge that I’d love to be able to work on.</p>

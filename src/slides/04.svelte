<h1>About the opportunity</h1>
<p>By now it should be clear why I wrote this. But there’s more.</p>
<p>
  These days, I’m looking less into stop building individual websites, and more
  into building tools that can build websites. Been working on leveling up my
  Node, Typescript and programming skills in general, in order to be able to
  contribute. There is a ton of progress happening in that area, and I
  definitely want to jump into that train.
</p>
<p>
  Tailwind CSS by itself is clearly a major player in the tools to build
  websites game, and feels to me like Tailwind UI could become a really big
  thing. With the kind of audience that you got, nothing would stop you from
  writing your own dynamic site generator, or replace PostCSS with a parser that
  suits your needs in a more tailored way. The sky seems to be the limit.
</p>
<p>
  That’s the kind of challenges that makes me feel awakie, and what I’d love to
  be able to participate into.
</p>

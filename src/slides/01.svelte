<h1>My thoughts on the web dev industry</h1>
<p>We seem to be at a turning point, pandemic aside.</p>
<p>
  While the past six or seven years have been all about growing up as an
  industry, lately more and more people seem to be thinking about what’s coming
  next. Is Javascript the ring that will rule us all forever? Is the Jamstack
  the best we can come up with for solving our problems? Or maybe it’s time for
  us to level up our practices, and incorporate ideas like giving the browser a
  break by compiling our code?
</p>
<p>
  No matter what the ends up happening, it’s pretty clear that the rise of tools
  like Next.js, Tailwind CSS, Netlify, Figma, Svelte, Snowpack and many others
  are helping the industry to move forward. We need to keep striving at shipping
  early and often while producing high-quality, performant websites.
</p>
<p>There’s work to be done. Exciting times are yet to come.</p>

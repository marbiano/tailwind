<h1>About me</h1>
<p>It’s been sixteen years since the first time I build a website.</p>
<p>
  Funny enough, the first thing I learnt was CSS. It was a pretty novel tool at
  that time, when the web was trying to wake up from the “build layouts with
  tables” nightmare. I remember doing full-stack dev before that even existed,
  using the good old PHP.
</p>
<p>
  I was around when Backbone revolutionized everything. When Angular was more
  hyped than Jennifer Lawrence. And finally when React became the new kid in the
  block. I remember thinking there was no way that a tool from Facebook would
  become mainstream. Boy I was wrong...
</p>
<p>
  I worked for many startups. Most of them failed, as expected. I worked for
  many digital agencies too. I also did my part at participating into projects
  where I did my best, only to never ever look back. The freelance life, you
  know?
</p>
<p>
  Around 2013 I got bored of coding only so after putting a lot of energy into
  teaching myself how to design an interface, I pursued the unicorn career.
  Tried if for a couple of years, but it was a bit too much for me. Also, jack
  of all trades, master of none, right?
</p>
<p>
  Nowadays, I’ve been working as a freelancer developer from project to project.
  Trying to find real purpose again. Some other interests came into my life, so
  the Big Question started to hunt my daily work: why am I doing what I’m doing?
</p>

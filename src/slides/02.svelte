<h1>A bit about me</h1>

<p>It’s been sixteen years since the first time I built a website.</p>
<p>
  Funny enough, the first thing I learned was CSS. It was a pretty novel tool
  when the web was trying to wake up from the “build layouts with tables”
  nightmare. I’m old enough to have done full-stack development before the term
  existed.
</p>
<p>
  I remember when Backbone changed everything, when Angular dethroned the king,
  when React became the new kid in the block. I’ve worked in many projects for
  startups, agencies and clients. At some point, I was caring so much about the
  UI that I decided to teach myself to design.
</p>
<p>
  Lately, I’ve been working primarily as a freelance frontend developer. But I’m
  missing a key ingredient in my work life, which is to have
  <strong>a real purpose</strong>
  that drives me into solving all kinds of problems.
</p>
